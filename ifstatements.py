# If Statements
# Programming & If Statements
# By learning programming, we can make the computer do anything that we want. 
# For example, we can write a program that shows the food menu for each day. 
# We can use conditions to display the food item depending on what day it is.

# Example:
# On Monday, "Rajma Chawal" will be made.
# On Tuesday, "Mutton Kosha" will be made.
# On Wednesday, "Pizza" will be made.
# On Thursday, "Dal - Roti" will be made.

# To do this in programming, we use if statements. 
# You can observe the applications of if statements in your daily life around you. 
# For example, Whatsapp shows two gray ticks when the message is not yet read by the receiver 
# and shows blue ticks when the message is read.


